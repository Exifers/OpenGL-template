#include "button.hh"


namespace gui
{
  void
  Button::mousePressed(int button, int x, int y)
  {
    button = button;
    x = x;
    y = y;
  }

  void
  Button::mouseReleased(int button, int x, int y)
  {
    button = button;
    x = x;
    y = y;
  }

  void
  Button::mouseMoved(int x, int y)
  {
    x = x;
    y = y;
  }

  void
  Button::display()
  {
  }
}
